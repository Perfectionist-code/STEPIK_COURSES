# Подвиг 9. (На использование цикла while). На вход программы подаются строки (названия книг). В программе уже реализовано их чтение и сохранение в списке (каждый элемент - название книги). После этого, из полученного списка нужно удалить все названия, состоящие из двух и более слов (слова разделяются пробелом). Результат выведите на экран в виде строки из оставшихся названий через пробел.
