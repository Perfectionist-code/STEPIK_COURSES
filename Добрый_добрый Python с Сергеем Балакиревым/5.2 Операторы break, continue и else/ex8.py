# Sample Input:
#
# Муму
# Евгений Онегин
# Сияние
# Мастер и Маргарита
# Пиковая дама
# Колобок
# Sample Output:
#
# Муму Сияние Колобок



# считывание списка из входного потока
import sys
lst_in = list(map(str.strip, sys.stdin.readlines()))
_len = len(lst_in)
i = 0
a = ' '
while i < _len:
    if a in lst_in[i]:
        lst_in.remove(lst_in[i])
        _len -= 1
        continue
    i += 1
print(*lst_in)